/**
 * Map language codes to short keys.
 * Accepts 'en', 'en-US', 'de', 'de-DE', 'ar', 'ar-SA', 'hi', 'hi-IN', etc.
 */
export function getLangKey(langCode) {
  if (!langCode) return 'en';
  const base = langCode.split('-')[0].toLowerCase();
  if (translations[base]) return base;
  return 'en';
}

/**
 * Get the BCP-47 code for a short lang key
 */
export function getLangBCP47(langKey) {
  const map = { en: 'en-US', de: 'de-DE', ar: 'ar-SA', hi: 'hi-IN' };
  return map[langKey] || 'en-US';
}

/**
 * Get text direction for a language
 */
export function getDirection(langKey) {
  return langKey === 'ar' ? 'rtl' : 'ltr';
}

/**
 * Create a translation function for a given language key
 */
export function createT(langKey) {
  const dict = translations[langKey] || translations.en;
  const fallback = translations.en;
  return (key, params = undefined) => {
    const template = dict[key] ?? fallback[key] ?? key;
    if (!params || typeof template !== 'string') return template;
    return Object.entries(params).reduce((acc, [k, v]) => {
      return acc.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v ?? ''));
    }, template);
  };
}

export default translations;
